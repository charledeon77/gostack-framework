/*
Purpose:
This file implements the make:event, make:job, make:policy, and make:provider
CLI scaffolding commands for the GoStack framework.

Philosophy:
Every subsystem should have a corresponding generator so developers never need to
write boilerplate from scratch. These generators follow the same pattern as the
existing make:controller and make:middleware commands — each produces a well-commented,
idiomatic Go source file under the appropriate internal/ subdirectory.

Architecture:
Implements console.Command. Each command uses text/template + go/format to produce
syntactically valid, package-correct Go source files.

Commands:
  - make:event    → internal/events/<Name>Event.go
  - make:job      → internal/jobs/<Name>Job.go
  - make:policy   → internal/policies/<Name>Policy.go
  - make:provider → internal/providers/<Name>ServiceProvider.go
*/
package console

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

// ─── make:event ────────────────────────────────────────────────────────────────

// MakeEventCommand scaffolds a new event class under internal/events/.
type MakeEventCommand struct{}

func (c *MakeEventCommand) Name() string        { return "make:event" }
func (c *MakeEventCommand) Description() string { return "Scaffold a new event class (Usage: make:event <Name>)" }

const eventStub = `// Code generated by GoStack make:event. Feel free to edit.
package events

import "time"

// {{.Name}}Event is raised when {{.Lower}} occurs in the application.
// Register listeners for this event in your EventServiceProvider.
type {{.Name}}Event struct {
	// OccurredAt records the time the event was raised.
	OccurredAt time.Time

	// TODO: Add your event payload fields here.
	// Example:
	//   UserID int
	//   Email  string
}

// New{{.Name}}Event creates a new {{.Name}}Event with the current timestamp.
func New{{.Name}}Event() *{{.Name}}Event {
	return &{{.Name}}Event{OccurredAt: time.Now()}
}
`

func (c *MakeEventCommand) Execute(args []string) error {
	if len(args) == 0 {
		return fmt.Errorf("usage: make:event <EventName>")
	}
	name := toStudlyCase(args[0])
	name = strings.TrimSuffix(name, "Event") // Normalize: avoid double "Event"

	dir := filepath.Join("internal", "events")
	if err := os.MkdirAll(dir, 0755); err != nil {
		return fmt.Errorf("failed to create directory %s: %w", dir, err)
	}

	filename := filepath.Join(dir, name+"Event.go")
	if _, err := os.Stat(filename); err == nil {
		return fmt.Errorf("event already exists: %s", filename)
	}

	data := struct {
		Name  string
		Lower string
	}{
		Name:  name,
		Lower: strings.ToLower(name),
	}
	return renderStub(filename, eventStub, data)
}

