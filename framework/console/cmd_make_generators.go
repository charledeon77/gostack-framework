// ─── Shared helpers ─────────────────────────────────────────────────────────────

// renderStub parses the template, executes it against data, formats the output
// with gofmt, and writes the result to filename.
func renderStub(filename, stubTemplate string, data any) error {
	tmpl, err := template.New("stub").Parse(stubTemplate)
	if err != nil {
		return fmt.Errorf("template parse error: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("template execute error: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// If gofmt fails (e.g. the stub uses placeholder comments), write raw.
		formatted = buf.Bytes()
	}

	if err := os.WriteFile(filename, formatted, 0644); err != nil {
		return fmt.Errorf("failed to write %s: %w", filename, err)
	}

	fmt.Printf("[GoStack CLI] Created: %s\n", filename)
	return nil
}

// toStudlyCase converts snake_case or kebab-case or plain words to StudlyCase.
// Examples: "user_profile" → "UserProfile", "send-email" → "SendEmail"
func toStudlyCase(s string) string {
	parts := strings.FieldsFunc(s, func(r rune) bool {
		return r == '_' || r == '-' || r == ' '
	})
	var b strings.Builder
	for _, p := range parts {
		if len(p) == 0 {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]) + p[1:])
	}
	return b.String()
}
